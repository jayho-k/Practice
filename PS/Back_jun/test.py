import time

start = time.time()
n,k = map(int,input().split())

workers = list(list(map(int,input().split())) for _ in range(n))
workers.sort()
t= workers[0][0] - 1


total = 0
while total<=k:

    t+=1
    for hour, work in workers:
        if t%hour ==0:
            total += work

print(t)

